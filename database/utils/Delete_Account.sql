WITH
	ACCOUNT_ID AS (
		SELECT
			ID
		FROM
			PRIVATE.ACCOUNTS
		WHERE
			NAME IN ('Gabi')
	),
	CARD_ID AS (
		SELECT
			ID
		FROM
			PRIVATE.CARDS
		WHERE
			ACCOUNT_ID IN (
				SELECT
					*
				FROM
					ACCOUNT_ID
			)
	),
	INVOICE_ID AS (
		SELECT
			ID
		FROM
			PRIVATE.INVOICES
		WHERE
			CARD_ID IN (
				SELECT
					*
				FROM
					CARD_ID
			)
	)
